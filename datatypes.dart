void main() {
  // remember Dart is case sensitive language.
  String firstName = "Satyam";
  int age = 23;
  double height = 5.8;
  bool likesDart = true;
  if (likesDart) {
    print(
        "$firstName who is $age years old and is ${height}ft tall likes dart language.");
  }
  // output: Satyam who is 23 years old and is 5.8ft tall likes dart language.
}
